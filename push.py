def get(day):
    if day in CONTENT: return CONTENT[day]
    return CONTENT[((day-1) % len(CONTENT)) + 1]

def sign():
    ts = str(round(time.time() * 1000))
    raw = ts + "\n" + SECRET
    mac = hmac.new(SECRET.encode(), raw.encode(), hashlib.sha256).digest()
    return ts, urllib.parse.quote_plus(base64.b64encode(mac).decode())

def send(title, text):
    ts, sig = sign()
    url  = WEBHOOK + "&timestamp=" + ts + "&sign=" + sig
    data = {"msgtype":"markdown","markdown":{"title":title,"text":text}}
    r = requests.post(url, json=data, timeout=10)
    print("结果:", r.json())

